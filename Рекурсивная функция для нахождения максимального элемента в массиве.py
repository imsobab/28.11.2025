def find_max(arr):
    """
    Рекурсивно находит максимальный элемент в массиве
    
    Args:
        arr: список чисел
    
    Returns:
        Максимальный элемент массива
    """
    # Базовый случай 1: пустой массив
    if len(arr) == 0:
        raise ValueError("Массив не может быть пустым")
    
    # Базовый случай 2: массив из одного элемента
    if len(arr) == 1:
        return arr[0]
    
    # Рекурсивный случай:
    # Сравниваем первый элемент с максимальным в оставшейся части
    # arr[0] - первый элемент
    # arr[1:] - оставшаяся часть массива
    # find_max(arr[1:]) - рекурсивно находим максимум в оставшейся части
    max_of_rest = find_max(arr[1:])
    
    # Возвращаем больший из первого элемента и максимума остальной части
    return arr[0] if arr[0] > max_of_rest else max_of_rest

# Примеры использования
print(find_max([3, 1, 4, 1, 5, 9, 2]))  # 9
print(find_max([-5, -2, -10, -1]))      # -1
print(find_max([42]))                   # 42

def sum_digits(n):
    """Рекурсивно вычисляет сумму цифр числа"""
    # Базовый случай: если число состоит из одной цифры
    if n < 10:
        return n
    # Рекурсивный случай: последняя цифра + сумма остальных цифр
    return n % 10 + sum_digits(n // 10)

# Примеры использования
print(sum_digits(123))    # 6 (1 + 2 + 3)
print(sum_digits(9876))   # 30 (9 + 8 + 7 + 6)
print(sum_digits(0))      # 0
print(sum_digits(1001))   # 2 (1 + 0 + 0 + 1)
